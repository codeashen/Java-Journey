create table AUTHORITY_PRODUCT
(
    ID            VARCHAR2(32) default SYS_GUID() not null
        primary key,
    PRODUCTNUM    VARCHAR2(50)                    not null,
    PRODUCTNAME   VARCHAR2(50),
    CITYNAME      VARCHAR2(50),
    DEPARTURETIME TIMESTAMP(6),
    PRODUCTPRICE  NUMBER,
    PRODUCTDESC   VARCHAR2(500),
    PRODUCTSTATUS NUMBER,
    constraint PRODUCT
        unique (ID, PRODUCTNUM)
)
/

comment on table AUTHORITY_PRODUCT is '产品表'
/

comment on column AUTHORITY_PRODUCT.ID is '主键uuid'
/

comment on column AUTHORITY_PRODUCT.PRODUCTNUM is '产品编号，唯一，不为空'
/

comment on column AUTHORITY_PRODUCT.PRODUCTNAME is '产品名称（路线名称）'
/

comment on column AUTHORITY_PRODUCT.CITYNAME is '出发城市'
/

comment on column AUTHORITY_PRODUCT.DEPARTURETIME is '出发时间'
/

comment on column AUTHORITY_PRODUCT.PRODUCTPRICE is '产品价格'
/

comment on column AUTHORITY_PRODUCT.PRODUCTDESC is '产品描述'
/

comment on column AUTHORITY_PRODUCT.PRODUCTSTATUS is '状态(0 关闭 1 开启)'
/

