create table AUTHORITY_USER
(
    ID       VARCHAR2(32) default SYS_GUID() not null
        primary key,
    EMAIL    VARCHAR2(50)                    not null
        unique,
    USERNAME VARCHAR2(50),
    PASSWORD VARCHAR2(200),
    PHONENUM VARCHAR2(20),
    STATUS   NUMBER
)
/

comment on table AUTHORITY_USER is '用户表'
/

comment on column AUTHORITY_USER.ID is '主键uuid'
/

comment on column AUTHORITY_USER.EMAIL is '邮箱，非空，唯一'
/

comment on column AUTHORITY_USER.USERNAME is '用户名'
/

comment on column AUTHORITY_USER.PASSWORD is '密码（加密）'
/

comment on column AUTHORITY_USER.PHONENUM is '电话'
/

comment on column AUTHORITY_USER.STATUS is '状态（0 未启用 1启用）'
/

