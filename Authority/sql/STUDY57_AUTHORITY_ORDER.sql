create table AUTHORITY_ORDER
(
    ID          VARCHAR2(32) default SYS_GUID() not null
        primary key,
    ORDERNUM    VARCHAR2(20)                    not null
        unique,
    ORDERTIME   TIMESTAMP(6),
    PEOPLECOUNT NUMBER,
    ORDERDESC   VARCHAR2(500),
    PAYTYPE     NUMBER,
    ORDERSTATUS NUMBER,
    PRODUCTID   VARCHAR2(32)
        references T_AUTHORITY_PRODUCT (),
    MEMBERID    VARCHAR2(32)
        references AUTHORITY_MEMBER
)
/

comment on table AUTHORITY_ORDER is '订单表'
/

comment on column AUTHORITY_ORDER.ID is '主键uuid'
/

comment on column AUTHORITY_ORDER.ORDERNUM is '订单编号，不为空，唯一'
/

comment on column AUTHORITY_ORDER.ORDERTIME is '下单时间'
/

comment on column AUTHORITY_ORDER.PEOPLECOUNT is '出行人数'
/

comment on column AUTHORITY_ORDER.ORDERDESC is '订单描述(其它信息)'
/

comment on column AUTHORITY_ORDER.PAYTYPE is '支付方式(0 支付宝 1 微信 2其它)'
/

comment on column AUTHORITY_ORDER.ORDERSTATUS is '订单状态(0 未支付 1 已支付)'
/

comment on column AUTHORITY_ORDER.PRODUCTID is '产品id 外键'
/

comment on column AUTHORITY_ORDER.MEMBERID is '会员(联系人）id 外键'
/

